package test.ch07.package1;

public class A {
	//필드
	protected String field;
	
	//생성자
	protected A() {
		
	}
	
	//메소드
	protected void method() {
		
	}
	
}
/*
public - 패키지 노상관
private - 같은 클래스 내에서만
default(안쓰는거) : 같은 패키지 내

protected - 상속 관계 |or|  같은 패키지 일때 접근 가능
필드, 생성자, 메소드 -- 적용가능
클래스 - 적용 불가 


*/